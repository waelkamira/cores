import mongoose from 'mongoose';

if (!process.env.NEXT_PUBLIC_Mongodb_url) {
  throw new Error('Please add your NEXT_PUBLIC_Mongodb_url to .env.local');
}

const NEXT_PUBLIC_Mongodb_url: string = process.env.NEXT_PUBLIC_Mongodb_url;

/**
 * Global is used here to maintain a cached connection across hot reloads
 * in development. This prevents connections growing exponentially
 * during API Route usage.
 */
let globalWithMongoose = global as typeof globalThis & {
  mongoose: any;
};
let cached = globalWithMongoose.mongoose;

if (!cached) {
  cached = globalWithMongoose.mongoose = { conn: null, promise: null };
}

async function dbConnect() {
  if (cached.conn) {
    return cached.conn;
  }

  if (!cached.promise) {
    const opts = {
      bufferCommands: false,
    };

    cached.promise = mongoose
      .connect(NEXT_PUBLIC_Mongodb_url, opts)
      .then((mongoose) => {
        return mongoose;
      });
  }
  cached.conn = await cached.promise;
  return cached.conn;
}

export default dbConnect;
